#include "libft.h"

void		*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*m;

	i = 0;
	m = b;
	while (len-- > 0)
	{
		m[i] = c;
		i++;
	}
	return (b);
}
