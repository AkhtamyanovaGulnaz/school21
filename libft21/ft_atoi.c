#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int n;
	int j;

	i = 0;
	j = 0;
	n = 1;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t'
			|| str[i] == '\r' || str[i] == '\f' || str[i] == '\v')
	{
		i++;
	}
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		n = -1;
		i++;
	}
	while (str[i] != '\0' && (str[i] >= '0' && str[i] <= '9'))
	{
		j = j * 10 + (str[i] - '0');
		i++;
	}
	return (j * n);
}
