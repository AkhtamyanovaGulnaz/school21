#include <unistd.h>

int	main(int argc, char **argv)
{
	int j;
	int s;

	(void)argc;
	j = 0;
	while (argv[0][j] != 0)
	{
		s = argv[0][j];
		write(1, &s, 1);
		j++;
	}
	write(1, "\n", 1);
	return (0);
}
