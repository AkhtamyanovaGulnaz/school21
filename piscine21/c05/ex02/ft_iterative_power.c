int	ft_iterative_power(int nb, int power)
{
	int i;
	int j;

	i = 1;
	j = 1;
	if (power > 0)
	{
		while (j <= power)
		{
			i = i * nb;
			j++;
		}
	}
	if (power < 0)
		i = 0;
	return (i);
}
