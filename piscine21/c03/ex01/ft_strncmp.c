int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int m;
	unsigned int i;

	i = 0;
	n--;
	while (s1[i] == s2[i] && i < n)
	{
		if (s1[i] == '\0')
		{
			return (0);
		}
		i++;
	}
	m = s1[i] - s2[i];
	return (m);
}
